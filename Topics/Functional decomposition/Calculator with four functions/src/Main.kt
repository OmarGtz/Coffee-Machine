fun subtractTwoNumbers(number1: Long, number2: Long) = println(number1 - number2)

fun sumTwoNumbers(number1: Long, number2: Long) = println(number1 + number2)

fun divideTwoNumbers(number1: Long, number2: Long) = println(if (number2 == 0L) "Division by 0!" else number1 / number2)

fun multiplyTwoNumbers(number1: Long, number2: Long) = println(number1 * number2)